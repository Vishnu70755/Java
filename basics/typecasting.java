class typecasting {
    public static void main(String[] args)
    {
        int implict=20000;
        double sal=implict;

        System.out.println("implict :"+implict);
        System.out.println("sal :"+sal);

        double car=12345678;// explict 
        int cash=(int)car;

        System.out.println("explict :"+car);
        System.out.println("cash :"+cash);


    }



    
}
